public class Profile {
    public String date;
    public String favourite;
    public String friend;
    public String gender;
    public String mom;
    public  String name;
    public String type;
    public  String word;
    public Profile(){

    }
    public Profile(String date, String favourite, String friend, String gender, String mom, String name, String type, String word){
        this.date = date;
        this.favourite = favourite;
        this.friend = friend;
        this.gender = gender;
        this.mom = mom;
        this.name = name;
        this.type = type;
        this. word = word;
    }
}
